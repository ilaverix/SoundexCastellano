	private double medidasimilitud(Character a,Character b)
	{
		return matriz[sonidos.get(a.toString())][sonidos.get(b.toString())];
	}
	
	private static String eliminarLetrasDuplicadas(String a)
	{
		char[] chars=a.toCharArray();
		
		Set<Character> charSet= new LinkedHashSet<Character>();
		
		for (char c: chars) {
			charSet.add(c);
		}
		
		StringBuilder sb = new StringBuilder();
		for(Character character : charSet)
		{
			sb.append(character);
		}
		
		return sb.toString();
	}
	

